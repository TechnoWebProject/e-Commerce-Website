	La surprise est une émotion provoquée par un événement ou une information inattendu. Elle est généralement brève, puis s'estompe ou laisse place à une autre émotion. Par métonymie, on appelle aussi surprise la cause de cette émotion.
		<br/><br/>
	Surprise et humour
	La surprise tient une place prépondérante dans l'humour. Elle prend souvent la forme d'une révélation soudaine qui donne une tout autre signification à une situation.
		<br/><br/>
	La surprise dans les arts
	La surprise est un élément de narration couramment utilisé dans le théâtre, le roman, le cinéma, etc. pour renverser une situation, afin d'éveiller l'intérêt du spectateur ou de soulager la tension narrative. Une surprise qui modifie radicalement la situation est appelée coup de théâtre.
